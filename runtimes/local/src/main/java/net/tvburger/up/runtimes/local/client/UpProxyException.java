package net.tvburger.up.runtimes.local.client;

public class UpProxyException extends RuntimeException {

    public UpProxyException() {
        super();
    }

    public UpProxyException(Throwable cause) {
        super(cause);
    }

    public UpProxyException(String message) {
        super(message);
    }

    public UpProxyException(String message, Throwable cause) {
        super(message, cause);
    }

}
