package net.tvburger.up.topology;

import net.tvburger.up.UpException;

public class TopologyException extends UpException {

    public TopologyException() {
        super();
    }

    public TopologyException(Throwable cause) {
        super(cause);
    }

    public TopologyException(String message) {
        super(message);
    }

    public TopologyException(String message, Throwable cause) {
        super(message, cause);
    }

}
